INTERP_RE = re.compile(r"\{\{[^}]+\}\}")
INTERP2_RE = re.compile(r"\{[a-zA-Z_][a-zA-Z0-9_]*\}")
HTML_RE = re.compile(r"</?[a-zA-Z][^>]*>")
PARAM_RE = re.compile(r":[a-zA-Z_][a-zA-Z0-9_]*\b")
PCT_RE = re.compile(r"%[sd]")
NUM_RE = re.compile(r"\b\d+([.,]\d+)?[%kMG]?\b")


def protect(text: str) -> tuple[str, list[str]]:
    """Replace tokens we must keep verbatim with sentinels."""
    tokens: list[str] = []

    def keep(m: re.Match) -> str:
        tokens.append(m.group(0))
        return f"\x00{len(tokens)-1}\x00"

    text = INTERP_RE.sub(keep, text)
    text = INTERP2_RE.sub(keep, text)
    text = HTML_RE.sub(keep, text)
    text = PARAM_RE.sub(keep, text)
    text = PCT_RE.sub(keep, text)
    # Protected technical terms (longest first to avoid partial matches)
    for term in sorted(PROTECTED, key=len, reverse=True):
        pat = re.compile(r"(?<!\w)" + re.escape(term) + r"(?!\w)")
        text = pat.sub(keep, text)
    return text, tokens


def restore(text: str, tokens: list[str]) -> str:
    def back(m: re.Match) -> str:
        idx = int(m.group(1))
        return tokens[idx] if 0 <= idx < len(tokens) else m.group(0)
    return re.sub(r"\x00(\d+)\x00", back, text)


