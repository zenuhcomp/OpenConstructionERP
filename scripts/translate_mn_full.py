def title_case(word: str) -> str:
    return word[0].upper() + word[1:] if word else word


def translate_word_aware(text: str) -> str:
    """Translate while preserving case of first letter for nouns/verbs."""
    def replace(m: re.Match) -> str:
        w = m.group(0)
        lw = w.lower()
        if lw in WORD_MAP:
            t = WORD_MAP[lw]
            if not t:
                return ""
            if w[0].isupper() and len(w) > 1:
                return title_case(t)
            return t
        return w

    return re.sub(r"\b[A-Za-z']+\b", replace, text)


def cleanup(text: str) -> str:
    """Tidy up double spaces and stray punctuation."""
    text = re.sub(r"\s{2,}", " ", text)
    text = re.sub(r"\s+([.,;:!?])", r"\1", text)
    text = text.strip()
    return text


def translate_entry(text: str) -> str:
    """Translate one string to Mongolian Cyrillic, best-effort."""
    if not text:
        return text
    stripped = text.strip()
    # 1) Exact phrase match (case-sensitive)
    if stripped in PHRASES:
        return PHRASES[stripped]
    # Try with trailing punctuation removed
    m = re.match(r"^(.+?)([.!?:…]+)$", stripped)
    if m and m.group(1) in PHRASES:
        return PHRASES[m.group(1)] + m.group(2)
    # 2) Protect tokens
    protected, tokens = protect(text)
    # 3) Phrase match again on the protected form (sentinels)
    if protected.strip() in PHRASES:
        return restore(PHRASES[protected.strip()], tokens)
    # 4) Word-level translation
    translated = translate_word_aware(protected)
    # 5) Restore protected tokens
    translated = restore(translated, tokens)
    # 6) Cleanup
    translated = cleanup(translated)
    return translated


# ---------------------------------------------------------------------------
# Main: read mn.ts, replace only the untranslated values, write back.
# ---------------------------------------------------------------------------
def main() -> None:
    with open(UNTRANS_JSON, encoding="utf-8") as f:
        untranslated = json.load(f)

    mn_text = MN_PATH.read_text(encoding="utf-8")

    # Build new translations
    new_translations: dict[str, str] = {}
    for k, en_v in untranslated.items():
        new_translations[k] = translate_entry(en_v)

    # Now replace in mn.ts. Each line is of form:
    #   "key": "value",
    # We must replace ONLY the untranslated lines (key -> exact en value).
    # Use a pass-through line rewriter.
    out_lines: list[str] = []
    pat = re.compile(r'^(\s*)"((?:[^"\\]|\\.)+)"\s*:\s*"((?:[^"\\]|\\.)*)"(,?)\s*$')

    count_replaced = 0
    for line in mn_text.splitlines(keepends=True):
        stripped = line.rstrip("\n").rstrip("\r")
        m = pat.match(stripped)
        if m:
            indent, key, value, comma = m.group(1), m.group(2), m.group(3), m.group(4)
            if key in new_translations:
                # Replace value with translation
                new_val = new_translations[key]
                # Escape backslashes and quotes for JSON-string embedding
                # NOTE: en value used the same escaping; we mirror it.
                # Translator outputs raw text; escape \\ and " for output.
                esc = new_val.replace("\\", "\\\\").replace('"', '\\"')
                new_line = f'{indent}"{key}": "{esc}"{comma}\n'
                out_lines.append(new_line)
                count_replaced += 1
                continue
        out_lines.append(line)

    MN_PATH.write_text("".join(out_lines), encoding="utf-8")
    print(f"Replaced {count_replaced} entries in mn.ts")


if __name__ == "__main__":
    main()
